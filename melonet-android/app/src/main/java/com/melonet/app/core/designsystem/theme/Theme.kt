@Composable
fun MeloNetTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme
    val meloNetColors = if (darkTheme) DarkMeloNetColors else LightMeloNetColors

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    CompositionLocalProvider(
        LocalSpacing provides Spacing(),
        LocalDimensions provides Dimensions(),
        LocalElevation provides Elevation(),
        LocalMeloNetColors provides meloNetColors,
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = MeloNetTypography,
            shapes = MeloNetShapes,
            content = content
        )
    }
}

object MeloNetTheme {
    val spacing: Spacing
        @Composable
        get() = LocalSpacing.current

    val dimensions: Dimensions
        @Composable
        get() = LocalDimensions.current

    val elevation: Elevation
        @Composable
        get() = LocalElevation.current

    val colors: MeloNetColors
        @Composable
        get() = LocalMeloNetColors.current
}

val LocalMeloNetColors = staticCompositionLocalOf { LightMeloNetColors }
